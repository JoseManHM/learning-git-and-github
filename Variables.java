
public class Variables {

	/**
	 * Programa que muestra el uso de variables
	 */
	public static void main(String[] args) {
		byte edad;
		edad = 20;
		System.out.println(edad);
		edad = 21;
		System.out.println(edad);
		edad = 22;
		System.out.println(edad);
		/*En las lineas anteriores podemos ver como el valor de la variable
		cambia a lo largo del programa*/
	}
}
